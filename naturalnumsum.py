# Python program to print the sum of the first ten natural numbers

# Number of terms
num_terms = 10

# Initialize sum
sum_of_natural_numbers = 0

# Calculate the sum using a loop
for i in range(1, num_terms + 1):
    sum_of_natural_numbers += i

# Display the result
print(f"The sum of the first {num_terms} natural numbers is {sum_of_natural_numbers}")

